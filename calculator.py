num1 = int(input("put your fav number"))
num2 = int(input("put your second fav number"))
op = input("+,-,*,/ wut are you gonna put")
print(num1, op, num2)

if op == "+":       
    print(num1 + num2)   
elif op == "-":       
    print(num1 - num2)   
elif op == "*":       
    print(num1 * num2)   
elif op == "/":       
    print(num1 / num2)   
